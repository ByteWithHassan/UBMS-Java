    private void btnViewReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewReportActionPerformed
         String searchMonth = JOptionPane.showInputDialog(
            this,
            "Enter Month:"
    );

    if (searchMonth == null ||
            searchMonth.trim().isEmpty()) {

        return;
    }

    ArrayList<Bill> bills = FileManager.loadBills();

    StringBuilder report = new StringBuilder();

    double totalRevenue = 0;

    boolean found = false;

    report.append("===== ").append(searchMonth.toUpperCase()).append(" REPORT =====\n\n");

    for (Bill bill : bills) {

        if (bill.getMonth().equalsIgnoreCase(searchMonth)) {

            found = true;

            report.append("Bill ID: ")
                    .append(bill.getBillId())
                    .append("\n");

            report.append("Customer ID: ")
                    .append(bill.getCustomerId())
                    .append("\n");

            report.append("Units: ")
                    .append(bill.getUnitsConsumed())
                    .append("\n");

            report.append("Amount: Rs. ")
                    .append(bill.calculateBill())
                    .append("\n");

            report.append("Status: ");

            if (bill.isPaid()) {

                report.append("PAID");
            }

            else {

                report.append("UNPAID");
            }

            report.append("\n\n");

            totalRevenue += bill.calculateBill();
        }
    }

    if (!found) {

        JOptionPane.showMessageDialog(this,
                "No bills found for this month!");

        return;
    }

    report.append("TOTAL REVENUE: Rs. ")
            .append(totalRevenue);

    JOptionPane.showMessageDialog(this,
            report.toString());
    }//GEN-LAST:event_btnViewReportActionPerformed

    private void btnSearchCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchCustomerActionPerformed
        String customerId = JOptionPane.showInputDialog(
            this,
            "Enter Customer ID:"
    );

    if (customerId == null || customerId.trim().isEmpty()) {

        return;
    }

    ArrayList<Customer> customers =FileManager.loadCustomers();

    ArrayList<Bill> bills =FileManager.loadBills();

    Customer foundCustomer = null;

    // Search customer
    for (Customer c : customers) {

        if (c.getId().equals(customerId)) {

            foundCustomer = c;
            break;
        }
    }

    // Customer not found
    if (foundCustomer == null) {

        JOptionPane.showMessageDialog(this,
                "Customer not found!");

        return;
    }

    // Build report
    StringBuilder report = new StringBuilder();

    report.append("===== CUSTOMER DETAILS =====\n\n");

    report.append("Customer ID: ")
            .append(foundCustomer.getId())
            .append("\n");

    report.append("Name: ")
            .append(foundCustomer.getName())
            .append("\n");

    report.append("Address: ")
            .append(foundCustomer.getAddress())
            .append("\n");

    report.append("Contact: ")
            .append(foundCustomer.getContact())
            .append("\n\n");

    report.append("===== CUSTOMER BILLS =====\n\n");

    boolean hasBills = false;

    for (Bill bill : bills) {

        if (bill.getCustomerId().equals(customerId)) {

            hasBills = true;

            report.append("Bill ID: ")
                    .append(bill.getBillId())
                    .append("\n");

            report.append("Month: ")
                    .append(bill.getMonth())
                    .append("\n");

            report.append("Units: ")
                    .append(bill.getUnitsConsumed())
                    .append("\n");

            report.append("Amount: Rs. ")
                    .append(bill.calculateBill())
                    .append("\n");

            report.append("Status: ");

            if (bill.isPaid()) {

                report.append("PAID");
            }

            else {

                report.append("UNPAID");
            }

            report.append("\n\n");
        }
    }

    if (!hasBills) {

        report.append("No bills found.");
    }

    JOptionPane.showMessageDialog(this,
            report.toString());
    }//GEN-LAST:event_btnSearchCustomerActionPerformed

