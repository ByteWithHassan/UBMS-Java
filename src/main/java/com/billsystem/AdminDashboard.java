    private void btnRemoveCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveCustomerActionPerformed

    
    String id = JOptionPane.showInputDialog(this, 
        "Enter Customer ID to delete:");
    
    if (id == null || id.trim().isEmpty()) {
        return;
    }
    
    id = id.trim();
    ArrayList<Customer> customers = FileManager.loadCustomers();
    
    boolean found = false;
    for (int i = 0; i < customers.size(); i++) {
        if (customers.get(i).getId().equals(id)) {
            customers.remove(i);
            found = true;
            break;
        }
    }
    
    if (found) {
        FileManager.saveAllCustomers(customers);
        JOptionPane.showMessageDialog(this, 
            "Customer Deleted Successfully!");
    } else {
        JOptionPane.showMessageDialog(this, 
            "Customer ID not found!");
    }

    }//GEN-LAST:event_btnRemoveCustomerActionPerformed

