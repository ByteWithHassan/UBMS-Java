private int attempts=0;
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     String id=username.getText().trim();
     String password = new String(password2.getPassword());
     if(id.isEmpty()||password.isEmpty()){
          JOptionPane.showMessageDialog(this, "Please enter both User ID and Password!");
          return;
     }
     Admin admin = new Admin("A001", "SystemAdmin", "admin123");
    
        // Loading customers 
    ArrayList<Customer> customers = FileManager.loadCustomers();

    for (Customer customer : customers) {
    if (id.equals(customer.getId()) && customer.login(password)) {

        JOptionPane.showMessageDialog(this, "Customer Login Successful!");

       new CustomerDashboard(customer).setVisible(true);
        this.dispose();
        return;
    }
}
    if (id.equals(admin.getId()) && admin.login(password)) {
        
        JOptionPane.showMessageDialog(this, "Welcome Admin! Login Successful.");
        
     
        new AdminDashboard().setVisible(true);
        
        
        this.dispose(); 
    }
    else {
        attempts++; // Increase failed attempt count
        
        int remaining = 3 - attempts;
        
        if (remaining > 0) {
            JOptionPane.showMessageDialog(this, 
                "Invalid Credentials!\nAttempts Remaining: " + remaining);
            
            
           username.setText("");
           password2.setText("");
           username.requestFocus();
        } 
        else {
           
            JOptionPane.showMessageDialog(this, 
                "Account Locked!\nToo many failed attempts.\nSystem will exit.");
            System.exit(0); // Close the whole program
        }
    }

    }//GEN-LAST:event_jButton1ActionPerformed

