    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = txtCustomerId.getText().trim();
String name = txtName.getText().trim();
String pass = new String(txtPassword.getPassword());
String address = txtAddress.getText().trim();
String contact = txtContact.getText().trim();

if (id.isEmpty() || name.isEmpty() || pass.isEmpty()
        || address.isEmpty() || contact.isEmpty()) {

    JOptionPane.showMessageDialog(this, "Please fill all fields!");
    return;
}

// Load existing customers
ArrayList<Customer> customers = FileManager.loadCustomers();

// Check duplicate ID
for (Customer c : customers) {
    if (c.getId().equals(id)) {
        JOptionPane.showMessageDialog(this, "Customer ID already exists!");
        return;
    }
}

// Create customer
Customer customer = new Customer(id, name, pass, address, contact);

// Save to file
FileManager.saveCustomer(customer);

JOptionPane.showMessageDialog(this, "Customer Added Successfully!");

this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

