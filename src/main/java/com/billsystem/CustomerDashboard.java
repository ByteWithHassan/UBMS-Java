    private void btnPayBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayBillActionPerformed
String billId = JOptionPane.showInputDialog(
            this,
            "Enter Bill ID to Pay:"
    );

    if (billId == null || billId.trim().isEmpty()) {
    JOptionPane.showMessageDialog(this,"Bill ID can not be empty!!");
        return;
    }

    ArrayList<Bill> bills = FileManager.loadBills();

    boolean found = false;

    for (Bill bill : bills) {

        // Customer can only pay own bills
        if (bill.getBillId().equals(billId)&& bill.getCustomerId().equals(customer.getId())) {

            found = true;

            if (bill.isPaid()) {

                JOptionPane.showMessageDialog(this,"Bill already paid!");
            }

            else {

                bill.setPaid();

                FileManager.saveAllBills(bills);

                JOptionPane.showMessageDialog(this,
                        "Bill Paid Successfully!");
            }

            break;
        }
    }

    if (!found) {

        JOptionPane.showMessageDialog(this,
                "Bill not found!");
    }      
    
    }//GEN-LAST:event_btnPayBillActionPerformed

    private void btnViewBillsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewBillsActionPerformed
   ArrayList<Bill> allBills = FileManager.loadBills();

    // Filter bills for current customer
    StringBuilder report = new StringBuilder();
    report.append("===== YOUR BILLS =====\n\n");

    boolean found = false;

    for (Bill bill : allBills) {

        if (bill.getCustomerId().equals(customer.getId())) {

            found = true;

            report.append("Bill ID: ").append(bill.getBillId()).append("\n");
            report.append("Month: ").append(bill.getMonth()).append("\n");
            report.append("Units: ").append(bill.getUnitsConsumed()).append("\n");
            report.append("Amount: Rs. ").append(bill.calculateBill()).append("\n");
            report.append("Status: ").append(bill.isPaid() ? "PAID" : "UNPAID").append("\n");
            report.append("--------------------------\n");
        }
    }

    if (!found) {
        report.append("No bills found for your account.");
    }

    JOptionPane.showMessageDialog(this, report.toString());
    }//GEN-LAST:event_btnViewBillsActionPerformed

